	const MiB = 1024 * KiB;
	const GiB = 1024 * MiB;
	const TiB = 1024 * GiB;
	const PiB = 1024 * TiB;

	const KB = 1000 * B;
	const MB = 1000 * KB;
	const GB = 1000 * MB;
	const TB = 1000 * GB;
	const PB = 1000 * TB;

	const Sec = 1;
	const mSec = 1e-3;
	const uSec = 1e-6;
	const nSec = 1e-9;
	const Min = 60;
	const Hour = 60 * Min;
	const Hours = Hour;
	const Day = 24 * Hours;
	const Days = Day;

}